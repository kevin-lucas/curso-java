public class Variaveis {

    public static void main(String args[]) {

        // Formas de declarar variáveis
        String primeiroNome = "Kevin";
        String segundoNome = "Lucas";
        String meuNome = "", meuOutroNome = "";

        // Variável que contém valores literais
        String nome = "Kevin Lucas";

        // Variável que contém valores inteiros
        int idade = 20;

        // Variáveis que contém valores lógicos booleanos
        boolean casado = false;

        // Imprime as informações
        System.out.println("Meu nome é " + nome + "e minha idade é " + idade);
    }
}
